#include "vga.h"

static uint8_t* g_vga_buffer;

void vga_disable_cursor() 
{
    ioport_out(0x3D4, 10);
    ioport_out(0x3D5, 32);
}

void set_miscellaneous_registers() 
{
    ioport_out(VGA_MISC_WRITE, 0x63);
}

void set_sequencer_registers() 
{
    ioport_out(VGA_SEQ_INDEX, 0);    // sequencer index
    ioport_out(VGA_SEQ_DATA, 0x03);  // sequencer reset

    ioport_out(VGA_SEQ_INDEX, 1);    // sequencer index
    ioport_out(VGA_SEQ_DATA, 0x01);  // clocking mode

    ioport_out(VGA_SEQ_INDEX, 2);    // sequencer index
    ioport_out(VGA_SEQ_DATA, 0x0F);  // plane/map mask

    ioport_out(VGA_SEQ_INDEX, 3);    // sequencer index
    ioport_out(VGA_SEQ_DATA, 0x00);  // character font

    ioport_out(VGA_SEQ_INDEX, 4);    // sequencer index
    ioport_out(VGA_SEQ_DATA, 0x0E);  // memory mode register
}

void set_graphics_controller_registers() 
{
    ioport_out(VGA_GC_INDEX, 0);    // gc index
    ioport_out(VGA_GC_DATA, 0x00);  // set/reset register

    ioport_out(VGA_GC_INDEX, 1);    // gc index
    ioport_out(VGA_GC_DATA, 0x00);  // enable set/reset register

    ioport_out(VGA_GC_INDEX, 2);    // gc index
    ioport_out(VGA_GC_DATA, 0x00);  // color compare register

    ioport_out(VGA_GC_INDEX, 3);    // gc index
    ioport_out(VGA_GC_DATA, 0x00);  // data rotate register

    ioport_out(VGA_GC_INDEX, 4);    // gc index
    ioport_out(VGA_GC_DATA, 0x00);  // read plane select register

    ioport_out(VGA_GC_INDEX, 5);    // gc index
    ioport_out(VGA_GC_DATA, 0x40);  // graphics mode register

    ioport_out(VGA_GC_INDEX, 6);    // gc index
    ioport_out(VGA_GC_DATA, 0x05);  // miscellaneous register

    ioport_out(VGA_GC_INDEX, 7);    // gc index
    ioport_out(VGA_GC_DATA, 0x0F);  // color dont care register

    ioport_out(VGA_GC_INDEX, 8);    // gc index
    ioport_out(VGA_GC_DATA, 0xFF);  // bit mask register
}

void set_attribute_controller_registers() 
{
    uint8_t d;
    uint8_t ac_data[21] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
                         0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
                         0x41, 0x00, 0x0F, 0x00, 0x00};

    for (uint8_t index = 0; index < 21; index++) {
        ioport_out(VGA_AC_INDEX, index);
        ioport_out(VGA_AC_WRITE, ac_data[index]);
    }
    d = ioport_in(VGA_INSTAT_READ);
    ioport_out(VGA_AC_INDEX, d | 0x20);
}

void set_crt_controller_registers() 
{
    uint8_t crtc_data[25] = {0x5F, 0x4F, 0x50, 0x82, 0x54, 0x80, 0xBF, 0x1F,
                            0x00, 0x41, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                            0x9C, 0x0E, 0x8F, 0x28, 0x40, 0x96, 0xB9, 0xA3, 0xFF};
    
    for (uint8_t index = 0; index < 25; index++) {
        ioport_out(VGA_CRTC_INDEX, index);
        ioport_out(VGA_CRTC_DATA, crtc_data[index]);
    }
}

void vga_graphics_init() 
{
    set_miscellaneous_registers();
    set_sequencer_registers();
    set_crt_controller_registers();
    set_graphics_controller_registers();
    set_attribute_controller_registers();

    g_vga_buffer = (uint8_t*)VGA_ADDRESS;

    vga_graphics_clear_color(BLACK);
}

void vga_graphics_clear_color(uint8_t color) {
    for (uint32_t index = 0; index < VGA_MAX; index++)
        g_vga_buffer[index] = color;
}

void vga_graphics_putpixel(uint16_t x, uint16_t y, uint8_t color) 
{
    uint32_t index = 0;
    index = 320 * y + x;
    if (index < VGA_MAX)
        g_vga_buffer[index] = color;
}

void vga_graphics_draw_line(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint8_t color)
{
	if (y1 == y2) {
        for (uint16_t i = x1; i <= x2; i++)
            vga_graphics_putpixel(i, y1, color);
        return;
    }

    if (x1 == x2) {
        for (uint16_t i = y1; i <= y2; i++) {
            vga_graphics_putpixel(x1, i, color);
        }
        return;
    }
}

void vga_graphics_draw_rect(uint16_t x, uint16_t y, uint16_t width, uint16_t height, uint8_t color)
{
	vga_graphics_draw_line(x, y, x, y + height, color);
    vga_graphics_draw_line(x, y, x + width, y, color);
    vga_graphics_draw_line(x + width, y, x + width, y + height, color);
    vga_graphics_draw_line(x, y + height, x + width, y + height, color);
}

void vga_graphics_fill_rect(uint16_t x, uint16_t y, uint16_t width, uint16_t height, uint8_t color)
{
	vga_graphics_draw_line(x, y, x, y + height, color);
    vga_graphics_draw_line(x, y, x + width, y, color);
    vga_graphics_draw_line(x + width, y, x + width, y + height, color);
    vga_graphics_draw_line(x, y + height, x + width, y + height, color);
    for (int i = 0; i < height; i++) 
    {
        vga_graphics_draw_line(x, y + i, x + width, y + i, color);
    }
}

void draw_bresenham_circle(int xc, int yc, int x, int y, uint8_t color) 
{
    vga_graphics_putpixel(xc + x, yc + y, color);
    vga_graphics_putpixel(xc - x, yc + y, color);
    vga_graphics_putpixel(xc + x, yc - y, color);
    vga_graphics_putpixel(xc - x, yc - y, color);
    vga_graphics_putpixel(xc + y, yc + x, color);
    vga_graphics_putpixel(xc - y, yc + x, color);
    vga_graphics_putpixel(xc + y, yc - x, color);
    vga_graphics_putpixel(xc - y, yc - x, color);
}

void vga_graphics_draw_circle(uint16_t x, uint16_t y, uint16_t radius, uint8_t color)
{
	int x2 = 0, y2 = radius;
    int d = 3 - 2 * radius;
    draw_bresenham_circle(x, y, x2, y2, color);
    while (y2 >= x2) {
        x2++;
        if (d > 0) {
            y2--;
            d = d + 4 * (x2 - y2) + 10;
        } else {
            d = d + 4 * x2 + 6;
        }
        draw_bresenham_circle(x, y, x2, y2, color);
    }
}

